# Shopping List

# groceries is list of groceries that were bought in the supermarket.
# Iterate through it using a for loop, and print “I bought 
# <name>!” for each one, in a new line.
# Remember to choose a good name for your loop variable.

groceries = ["Milk", "Eggs", "Bread", "Apples", "Carrots", "Chicken", "Rice"]
for item in groceries:
    print(f"I bought {item}")
