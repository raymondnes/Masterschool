# Count Function

# Write a function called count_to_n, that takes an argument n (int).
# The function will print the numbers from 0 up to but not 
# including n using a for loop and the range function.

def count_to_n(n):
#   n = int(input("Enter a number: "))
    for i in range(n):
        print(i)


