# Introduce yourself

# Write a function introduce that takes two arguments: a name 
# (str) and an age (int). The function should print a message 
# like "Hello, my name is Alice and I'm 25 years old." Don't 
# forget to call the function with different names and ages.

def introduce(name, age):
    print(f"Hello, my name is {name} and I'm {age} years old.")

introduce("Alice", 25)
