# Write a function describe_pet that takes two arguments: 
# animal_type (str) and pet_name (str). The function should 
# print a message like "I have a dog named Rover." Try it with 
# different animal types and pet names.

def describe_pet(animal_type, pet_name):
    print(f"I have a {animal_type} named {pet_name}")


